namespace PetWebsite.Domain.Enums;

/// <summary>
/// Represents the size category of a pet.
/// </summary>
public enum PetSize
{
	/// <summary>
	/// Extra small pets (e.g., hamsters, small birds).
	/// </summary>
	ExtraSmall = 1,

	/// <summary>
	/// Small pets (e.g., Chihuahua, small cats).
	/// </summary>
	Small = 2,

	/// <summary>
	/// Medium-sized pets (e.g., Beagle, standard cats).
	/// </summary>
	Medium = 3,

	/// <summary>
	/// Large pets (e.g., Labrador, German Shepherd).
	/// </summary>
	Large = 4,

	/// <summary>
	/// Extra large pets (e.g., Great Dane, Saint Bernard).
	/// </summary>
	ExtraLarge = 5,
}
